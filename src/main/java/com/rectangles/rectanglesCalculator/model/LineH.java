package com.rectangles.rectanglesCalculator.model;

public class LineH {
   private Integer x1;
   private Integer x2;
   private Integer y;

    public LineH(Integer x1, Integer x2, Integer y) {
        this.x1 = x1;
        this.x2 = x2;
        this.y = y;
    }

    public Integer getX1() {
        return x1;
    }

    public void setX1(Integer x1) {
        this.x1 = x1;
    }

    public Integer getX2() {
        return x2;
    }

    public void setX2(Integer x2) {
        this.x2 = x2;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
